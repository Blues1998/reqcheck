# Use sys.stdlib_module_names if available (Python 3.10+), else fall back
_STDLIB: frozenset[str] = (
    sys.stdlib_module_names  # type: ignore[attr-defined]
    if hasattr(sys, "stdlib_module_names")
    else _STDLIB_39
)

# Also include built-in module names (C extensions compiled into the interpreter)
_BUILTINS: frozenset[str] = frozenset(sys.builtin_module_names)


def is_stdlib_module(module_name: str) -> bool:
    """Return True if module_name is part of the Python standard library."""
    top_level = module_name.split(".")[0]
    return top_level in _STDLIB or top_level in _BUILTINS
